    document.querySelectorAll('[data-date]').forEach(el => {
      const targetDate = new Date(el.getAttribute('data-date'));
      const now = new Date();

      // Ajustamos la zona horaria para no tener problemas con la diferencia horaria
      now.setHours(0, 0, 0, 0); // Establecer la hora del día de hoy a medianoche para obtener solo la diferencia de días

      const timeDiff = targetDate - now;
      const days = Math.max(0, Math.ceil(timeDiff / (1000 * 60 * 60 * 24))); // Calcula la cantidad de días restantes

      el.textContent = days;

      // Cambia la palabra 'día' o 'días' en singular o plural según corresponda
      const daysText = days === 1 ? 'día' : 'días';
      const daysElement = el.nextElementSibling;
      daysElement.textContent = daysText;

      // Asegura que 'días' tenga el mismo tamaño que la cantidad
      daysElement.classList.add('fs-4');
    });
